package ivr.utils;

public class GroupzMemberInfo {

	protected String division;

	protected String memberCode;
	protected int memberId;
	protected String memberUniqueId;

	public String getDivision() {
		return division;
	}

	public void setDivision(String value) {
		this.division = value;
	}

	public String getMemberCode() {
		return memberCode;
	}

	public void setMemberCode(String value) {
		this.memberCode = value;
	}

	public int getMemberId() {
		return memberId;
	}

	public void setMemberId(int value) {
		this.memberId = value;
	}

	public String getMemberUniqueId() {
		return memberUniqueId;
	}

	public void setMemberUniqueId(String value) {
		this.memberUniqueId = value;
	}

}
